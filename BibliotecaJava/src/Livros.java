public class Livros {
    String titulo;
    int id;
    String categoria;
    String autor;
    int paginas;

    Livros(String titulo, int id, String categoria, String autor, int paginas) {
        this.titulo = titulo;
        this.id = id;
        this.categoria = categoria;
        this.autor = autor;
        this.paginas = paginas;
    }
}